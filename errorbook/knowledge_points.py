def classify_knowledge_point(kp, subject):
    """将AI返回的细粒度知识点归类到一级知识点，无法归类则返回None"""
    if not kp or not subject or subject not in KNOWLEDGE_TREE:
        return None
    first_level_kps = KNOWLEDGE_TREE[subject]
    # 精确匹配
    if kp in first_level_kps:
        return kp
    # 检查别名映射
    aliases = _ALIAS_MAP.get(subject, {})
    for first_level, keywords in aliases.items():
        for kw in keywords:
            if kw in kp or kp in kw:
                return first_level
    # 检查一级知识点名称是否包含在AI知识点中
    for first_level in first_level_kps:
        if first_level in kp or kp in first_level:
            return first_level
    return None
